# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas possiveis: (True, False)
# Existem vários operadores para "qustionar"
# Dentre eles o ==, que é um operador lógico qaue questiona se um valor 'e igual a outro

print(10 == 10)  # sim => True (verdadeiro)
print(10 == 15)  # nao => False (Falso)
print(type(10 == 10))
